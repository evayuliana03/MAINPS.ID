// ===========================
// Ambil Element Container Game
// ===========================

const container =
  document.getElementById(
    "gameContainer"
  );

// ===========================
// Render / Menampilkan Game
// ===========================

function tampilGame(data) {

  container.innerHTML = "";

  data.forEach(game => {

    container.innerHTML += `

      <div class="col-lg-3 col-md-4 col-6">

        <div class="game-item">

          <img
            src="${game.image}"
            class="game-img">

          <div class="game-title">
            ${game.name}
          </div>

          <div class="game-rating">
            ⭐ ${game.rating}
          </div>

        </div>

      </div>

    `;
  });

}

// =================================
// Tampilan pertama dan Search Game
// =================================

tampilGame(games);

document
  .getElementById(
    "searchGame"
  )
  .addEventListener(
    "keyup",
    function () {

      const keyword =
        this.value.toLowerCase();

      const hasil =
        games.filter(game =>

          game.name
            .toLowerCase()
            .includes(keyword)

        );

      tampilGame(hasil);

    });